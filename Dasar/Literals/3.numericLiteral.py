# Integer Literal

# Positive
p = 123
# Negative
n = -123
# Nol
z = 0
# Bilangan floating-point
f = 3.15
# Bilangan floating-point positif yang ditulis dalam notasi ilmiah
fp = 4.2e-3
# Binary
a = 0b10100
# Decimal
b = 50
# Octal
c = 0o320
# Hexadecimal
d = 0x12b
# Bilangan kompleks dengan bagian real 2 dan bagian imajiner 3
k = 2 + 3j
# Bilangan kompleks dengan bagian real -1.5 dan bagian imajiner -2.5
kr = 1.5 - 2.5j

print(p, n, z, f, fp, a, b, c, d, k, kr)
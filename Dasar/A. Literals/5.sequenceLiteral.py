# Sequence (Container) Literal

# List literal
belanjaan = ['sayur', 'telur', 'buah']
print("List Belanjaan:", belanjaan)

# Set Literal
koordinat = {(0, 0), (1, 1), (2, -2)}
print("Set Koordinat:", koordinat)

#  Dictionary literal
orang = {"Nama": "Abie", "Umur": "19", "Kota": "Jogja"}
print("Nama :", orang["Nama"])
print("Umur :", orang["Umur"])
print("Kota :", orang["Kota"])  

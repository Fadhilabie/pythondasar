# Numerik Literal

# Integer Positive
p = 123
# Integer Negative
n = -123
# Bilangan Bulat Nol
z = 0
# Bilangan Titik Desimal
f = 3.15
# Bilangan floating-point dalam notasi ilmiah
fp = 4.2e-3
# Bilangan Binary
a = 0b10100
# Bilangan Decimal
b = 50
# Bilangan Octal
c = 0o320
# Bilangan Hexadecimal
d = 0x12b
# Bilangan kompleks
k = 2 + 3j

print(p, n, z, f, fp, a, b, c, d, k)
# Casting Variabel

# int()
varFloat = 3.15
cast_ke_int = int(varFloat)
print(cast_ke_int)

# float()
varInt = 45
cast_ke_float = float(varInt)
print(cast_ke_float)

# str()
varInt = 76
cast_ke_str = str(varInt)
print(cast_ke_str)   

# bool()
varInt = 1
cast_ke_bool = bool(varInt)
print(cast_ke_bool)
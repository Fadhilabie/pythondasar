# Memberikan akses ke berbagai fungsi matematika dan konstanta matematika yang umum digunakan.
import math

# Menghitung akar kuadrat
akar = math.sqrt(16)
print(akar)

# Menghitung logaritma basis 10
log_10 = math.log10(100)
print(log_10)

# Menghitung sin, cos, dan tan
sudut = math.radians(30)  # Mengubah sudut ke radian
sin = math.sin(sudut)
cos = math.cos(sudut)
tan = math.tan(sudut)
print(sin, cos, tan)
